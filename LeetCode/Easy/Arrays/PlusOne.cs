namespace LeetCode.Easy.Arrays;

/// <summary>
/// 66. Plus One
/// Условие: дан массив цифр, представляющий неотрицательное целое число. Вернуть массив, представляющий число + 1.
///
/// Подход:
/// 1. Идём с конца массива.
/// 2. Если цифра < 9 → просто увеличиваем на 1 и возвращаем.
/// 3. Если цифра == 9 → превращаем в 0 и продолжаем (перенос 1).
/// 4. Если все цифры были 9 → создаём новый массив с 1 в начале.
///
/// Сложность:
/// - Время: O(n) в худшем случае (когда все цифры 9)
/// - Память: O(n) в худшем случае (когда нужен новый массив)
///
/// Пример 1:
/// Input: digits = [1,2,3]
/// Output: [1,2,4]
/// Объяснение: 123 + 1 = 124
///
/// Пример 2:
/// Input: digits = [9,9]
/// Output: [1,0,0]
/// Объяснение: 99 + 1 = 100, нужен новый массив большего размера
/// </summary>
public class PlusOne12
{
    public int[] PlusOne(int[] digits)
    {
        // идём с конца массива
        for (int i = digits.Length - 1; i >= 0; i--)
        {
            // если цифра меньше 9 — просто +1 и готово
            if (digits[i] < 9)
            {
                digits[i]++;
                return digits;
            }
            
            // если цифра была 9 — превращаем её в 0 и переносим 1 дальше
            digits[i] = 0;
        }
        
        // если мы здесь — значит ВСЕ цифры были 9
        // создаём новый массив: [1, 0, 0, ...]
        int[] result = new int[digits.Length + 1];
        result[0] = 1;

        return result;
    }
}