namespace LeetCode.Easy.DynamicProgramming;

/// <summary>
/// 70. Climbing Stairs
/// Условие: подняться на n ступенек. Можно делать шаг на 1 или 2 ступеньки за раз. Вернуть количество способов.
///
/// Подход (динамическое программирование):
/// 1. ways[i] — количество способов добраться до ступеньки i.
/// 2. Базовые случаи: ways[1] = 1, ways[2] = 2.
/// 3. Для i >= 3: ways[i] = ways[i-1] + ways[i-2].
///    - Можно прийти с (i-1)-й ступеньки (шаг 1).
///    - Можно прийти с (i-2)-й ступеньки (шаг 2).
///
/// Сложность:
/// - Время: O(n)
/// - Память: O(n) для массива ways
///
/// Пример:
/// Input: n = 3
/// Output: 3
/// Объяснение:
/// - Способ 1: 1 шаг + 1 шаг + 1 шаг
/// - Способ 2: 1 шаг + 2 шага
/// - Способ 3: 2 шага + 1 шаг
/// </summary>
public class ClimbStairs2
{
    public int ClimbStairs(int n)
    {
        if (n <= 2)
            return n;

        int[] ways = new int[n + 1];
        ways[1] = 1;
        ways[2] = 2;

        for (int i = 3; i <= n; i++)
        {
            ways[i] = ways[i - 1] + ways[i - 2];
        }

        return ways[n];
    }

}